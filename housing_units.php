<h2>What?</h2>
<p>This Project was in conjunction with my job at the time, working for a freelance director.</p>
<p>The brief was that Housing Units, a department store based within Manchester, wanted a christmas advertisement creating in order to create more revenue before the festive period. This advert was eventually aired at prime time on ITV.</p>
<p>My role within this project was to be what is known in the industry as a 'runner'. This included the loading and unloading of various film equipment and helping the crew with all aspects of the development process. My further role on this project was also to control the on set music so that the choreography would be in time with the music to be overlayed on the advert.</p>
