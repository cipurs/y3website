<h2>What?</h2>
<p>This Project was in conjunction with my job at the time, working for a freelance director.</p>
<p>The brief was that Sale Sharks had just finished a transfer to sign a prop, Brian Mujati, and as such they were interested in being the first rugby league team to create an unveiling video for the fans to announce the arrival of the new player.</p>
<p>My role in the making if this promotional video was to assisst with the set lighting and to be what is known in the industry as a 'runner'. This included the loading and unloading of various film equipment as well as having a go at a tempermental smoke machine as well as being in charge of food and drink... what a job!</p>
