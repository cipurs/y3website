<h1>The rise of the industry… The decline of local talent?</h1>
<hr>
<h2>Rise of Industry?</h2>
<p>Rise of industry?
I think that it is a given that the digital industry as a whole is experiencing an exponential growth since the release of everyday affordable technology such as smartphones and social media. With this growth of use obviously comes a growth of people who maintain or create content for the masses to enjoy. In the North West alone, most digital agencies, be it website or just general design, claimed to experience a staggering growth rate of 84% within the past 12 months!<a href=' http://www.techcityuk.com/technation/' title='technation report home'>(Tech Nation, 2016)</a>
</p>
<hr>
<img src='media/images/blog_industry/ind-2nd.png' alt='Image of a desktop motherboard to demonstrate advances in technology.'>
<hr>
<h2>Why the decline in local talent?</h2>
<p>Unfortunately, not all is as you would expect. 34% of agencies in the North West use agency talent with some outsourcing to local talent but a huge 26% had outsourced their development needs outside of the UK and even the EU. Further to this, 37% of agencies had had to turn away clients as they were unable to find the right talent! I personally found these statistics to be most worrying as we always hear about the industry experiencing exponential growth, when in reality the amount of people going into the industry from the North West is falling far short to be able to keep up with the demand for digital jobs.</p>
<hr>
<h2>Who are they actually hiring?</h2>
<p>According to Manchester Digital Skills Summit 2016, 17.8% of respondents to their survey revealed that they were hiring talent from universities outside of the North West with half of them saying it was due to the lack of quality talent. This being said however, the two universities out of the entire North West most likely to be picked up by local employers were University of Manchester and Manchester Metropolitan University. Being a student of MMU I find it somewhat comforting that agencies in the area are already aware of the large amount of talent coming out of local establishments. As for the mythical gender bias, it has been shown that within the North West there is generally a 60/40 split between males and females respectively which personally I fell is a huge step in the right direction for not only the digital industry but society as a whole!</p>
<hr>
<h2>Why is the talent not there?</h2>
<p>I don’t think that there is a lack of talent as such just a huge lack of confidence. As I have been learning how to develop websites I have come across the phenomenon that is imposter syndrome. Imposter Syndrome is where a person feels as if people think they’re better than they think their skills allow. For example, as mentioned in <a href=' https://davidwalsh.name/impostor-syndrome' title='Blog about imposter syndrome'>David Walsh’s blog</a>, in the digital industry everyone with a computer and internet access can be considered as a competitor. That coupled with the various techniques or ways of doing the same thing. The chances are whatever you have done someone is going to, if they haven’t already, find a quicker, more effective way of doing the same thing you’ve done. I feel as though because of this, it puts people off applying for certain things as they’re constantly doubting themselves and putting themselves down. This seems to be a common problem as even myself and people within my study group at university have said that even though we have the necessary skills required for entry level work we feel as though we aren’t yet ready for work in the industry.</p>
<hr>
<img src='media/images/blog_industry/ind-imp.png' alt='Portrait of a person holding their head whilst wearing a mask so as to hide their identity.'>
<hr>
<h2>What’s being done to fix this?</h2>
<p>On a more personal level I know that Manchester Metropolitan University is always encouraging us to attend industry events such as NUX and Word Camp. All these types of events give a helpful insight into the industry from the very people who are setting out to hire local talent. This coupled with industry guests such as members of the BBC’s UX team have certainly inspired me to consider a career in User Experience design but also gave us a look at the kind of tools and techniques industry experts are using to be the best in the North West. I feel as though being a student at MMU has given me the best possible start to my future career that I could’ve hoped for, given how involved the course is with the industry.</p>
