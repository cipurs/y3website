<h2>What?</h2>
<p>This Project was part of my university coursework for my second year course; Digital Project Management, to create a guide that a student may use.</p>
<p>The brief was that within teams of 6 people that were assigned to teams based in their skills and exoerience.</p>
<p>My role in this project was deputy project manager and I was also head of media. As such it was my task to assist the project leader to ensure we were still on schedule, but to also design the poster for the poster event, as well as gather media for the actual site. Our idea behind UpDog was to provide a service that showed students which events were on near them as well as to subconciosly deliver articles about how to stay safe out and about in Manchester. The name for the project was originally just a placeholder name but after a few jokes about it and lack of anything that fitted the project better we just ran with it!</p>
